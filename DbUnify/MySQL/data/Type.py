class DataType:
    INT = 'INT'
    INTEGER = 'INTEGER'
    TINYINT = 'TINYINT'
    SMALLINT = 'SMALLINT'
    MEDIUMINT = 'MEDIUMINT'
    BIGINT = 'BIGINT'
    UNSIGNED_BIG_INT = 'UNSIGNED BIG INT'
    TEXT = 'TEXT'
    VARCHAR = lambda length: f'VARCHAR({length})'
    CHAR = lambda length: f'CHAR({length})'
    DATE = 'DATE'
    DATETIME = 'DATETIME'
    DECIMAL = lambda precision, scale: f'DECIMAL({precision},{scale})'
    FLOAT = 'FLOAT'
    DOUBLE = 'DOUBLE'
    BOOLEAN = 'BOOLEAN'
    BLOB = 'BLOB'
    CLOB = 'CLOB'
